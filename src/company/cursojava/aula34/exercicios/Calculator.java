package company.cursojava.aula34.exercicios;

public class Calculator {
	// Write static methods for +, -, *, / and ^n two numbers
	public static int sum(int num1, int num2) {
		return num1 + num2;
	}
	
	public static double sum(int num1, double num2) {
		return num1 + num2;
	}
	
	public static double sum(double num1, double num2) {
		return num1 + num2;
	}
	
	public static int sub(int num1, int num2) {
		return num1 - num2;
	}
	
	public static double sub(int num1, double num2) {
		return num1 - num2;
	}
	
	public static double sub(double num1, double num2) {
		return num1 - num2;
	}
	
	public static int mult(int num1, int num2) {
		return num1 * num2;
	}
	
	public static double mult(int num1, double num2) {
		return num1 * num2;
	}
	
	public static double mult(double num1, double num2) {
		return num1 * num2;
	}
	
	public static int div(int num1, int num2) {
		return num1 / num2;
	}
	
	public static double div(int num1, double num2) {
		return num1 / num2;
	}
	
	public static double div(double num1, double num2) {
		return num1 / num2;
	}
	
}
